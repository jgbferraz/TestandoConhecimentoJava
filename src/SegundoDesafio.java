public class SegundoDesafio {
    public static void main(String[] args) {
        //Declare uma variável do tipo double e uma variável do tipo int.
        // Faça o casting da variável double para int e imprima o resultado.

        double primeiraVariavel = 22.70;
        int segundaVariavel = 0;

        segundaVariavel = (int) primeiraVariavel;

        System.out.println("*******************************************************************************");
        System.out.println("*******************************************************************************");
        System.out.println("O valor original da variável no formato de double: " + primeiraVariavel);
        System.out.println("O valor convertido para int: " + segundaVariavel);
        System.out.println("*******************************************************************************");
        System.out.println("*******************************************************************************");
    }
}
